package com.hspedu.abstract_;

public class AbstractDetail01 {
    /**抽象类详解
     * 1.抽象类不能被实例化（不能创建相应对象）
     * 2.抽象类不一定要包含抽象方法，而且可以包含非抽象方法
     * 3.只有抽象类才能包含抽象方法
     * 4.abstract只能修饰类和方法，不能修饰属性和其他
     * 5.抽象类可以有任意成员（抽象类本质上还是类），可以有非抽象方法，构造器，属性等等
     * 6.抽象方法没有主体，即没有实现
     * 7.如有一个类继承了抽象类，则它必须实现抽象类的所有抽象方法，除非它自己也声明为抽象类
     * 8.抽象方法不能用public，static，final来修饰，因为这些修饰符与重写相违背*/
}
